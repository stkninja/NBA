package vo;

import java.awt.Image;

public class PlayerVO {
	public String name;
	public Image portrait;                  //肖像
	public Image action;                    //动作照
	public String team;
	public String position;
	public String subArea;                  //分区
	public int gameplay;                    //参赛场数
	public int gamestart;                   //先发场数
	public int allrebound;                  //总篮板数
	public double rebound;                  //场均篮板数
	public double alloffensiverebound;      //总进攻篮板数
	public double offensiverebound;         //场均进攻篮板数
	public double alldefensiverebound;      //总防守篮板数
	public double defensiverebound;         //场均防守篮板数
	public int allassist;                   //总助攻数
	public double assist;                   //场均助攻数
	public double allminute;                //总在场时间
	public double minute;                   //场均在场时间
	public int alloffense;                  //总进攻数
    public double offense;                  //场均进攻数
	public int alldefence;                  //总防守数
	public double defence;                  //场均防守数
	public int allsteal;                    //总抢断数
	public double steal;                    //场均抢断数
	public int allblock;                    //总盖帽数
	public double block;                    //场均盖帽数
	public int allerror;                    //总失误数
	public double error;                    //场均失误数
	public int allfoul;                     //总犯规数
	public double foul;                     //场均犯规数
	public int allpoint;                    //总得分
	public double point;                    //场均得分
	public int allshoot;                    //总投篮出手数
	public double shoot;                    //场均投篮出手数
	public double allshootmade;             //总投篮命中数
	public double shootmade;                //场均投篮命中数
	public int allthreepoint;               //总三分出手数
	public double threepoint;               //场均三分出手数
	public double allthreepointmade;        //总三分命中数
	public double threepointmade;           //场均三分命中数
	public int allfreethrow;                //总罚球出手数
	public double freethrow;                //场均罚球出手数
	public double allfreethrowmade;         //总罚球命中数
	public double freethrowmade;            //场均罚球命中数
	public int doubledouble;                //两双
	public double allfieldgoalpercent;      //总投篮命中率
	public double fieldgoalpercent;         //场均投篮命中率
	public double allthreepointpercent; //总三分命中率
	public double threepointpercent; //场均三分命中率
	public double allfreethrowpercent;  //总罚球命中率
	public double freethrowpercent;  //场均罚球命中率
	public double allefficiency;    //总效率
	public double efficiency;    //场均效率
	public double allgmsc;          //总GmSc效率值
	public double gmsc;          //场均GmSc效率值
	public double allrealshootpercent;  //总真实命中率
	public double realshootpercent;  //场均真实命中率
	public double allshootefficiency;   //总投篮效率
	public double shootefficiency;   //场均投篮效率
	public double allreboundrate;       //总篮板率
	public double reboundrate;       //场均篮板率
	public double alloffensivereboundrate;  //总进攻篮板率
	public double offensivereboundrate;  //场均进攻篮板率
	public double alldefensivereboundrate;  //总防守篮板率
	public double defensivereboundrate;  //场均防守篮板率
	public double allassistrate;        //总助攻率
	public double assistrate;        //场均助攻率
	public double allstealrate;     //总抢断率
	public double stealrate;     //场均抢断率
	public double allblockrate;     //总盖帽率
	public double blockrate;     //场均盖帽率
	public double allerrorrate;     //总失误率
	public double errorrate;     //场均失误率
	public double allusage;         //总使用率
	public double usage;         //场均使用率
	
	public PlayerVO(){
		allfieldgoalpercent = allshootmade / allshoot;
		allthreepointpercent = allthreepointmade / allthreepoint;
		allfreethrowpercent = allfreethrowmade / allfreethrow;
		allefficiency = (allpoint + allrebound + allassist + allsteal + allblock) - (allshoot - allshootmade) - (allfreethrow - allfreethrowmade) - allerror;
		allgmsc = allpoint + 0.4 * allshootmade - 0.7 * allshoot - 0.4 * (allfreethrow - allfreethrowmade) + 0.7 * alloffensiverebound + 0.3 * alldefensiverebound + allsteal + 0.7 * allassist + 0.7 * allblock - 0.4 * allfoul - allerror;
		allrealshootpercent = allpoint / (2 * (allshoot + 0.44 * allfreethrow));
		allshootefficiency = (allshootmade + 0.5 * allthreepointmade) / allshoot;
		allerrorrate = allerror / (allshoot - allthreepoint + 0.44 * allfreethrow + allerror);
	}
}

